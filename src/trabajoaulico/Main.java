    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // Guardar
        
        if(jtDocumento.getText().isEmpty() || jtNombre.getText().isEmpty() || jtApellido.getText().isEmpty() || jtSueldo.getText().isEmpty()){
        
            JOptionPane.showMessageDialog(this, "Por favor, rellene los campos de texto...");
            
            return;
        
        }else{
        
            try{
                    
                    int documento = Integer.parseInt(jtDocumento.getText());
                    String nombre = jtNombre.getText();
                    String apellido = jtApellido.getText();
                    Categoria categoria = null;
                    double sueldo = Double.parseDouble(jtSueldo.getText());
                    Empresa empresa = null;
                    //String razonsocial = jtRazonSocial.getText();
                    //int cuit = Integer.parseInt(jtCuit.getText());
                    
                    
                    //Falta Empresa ComboBox
                    
                    if(jcCategoria.getSelectedIndex() == 0){
                    
                        categoria = Categoria.ADMINISTRATIVO;
                    
                    }else if(jcCategoria.getSelectedIndex() == 1){
                    
                        categoria = Categoria.GERENTE;
                    
                    }else{
                    
                        categoria = Categoria.JEFE;
                    
                    }
                    
                    Empleado emp = new Empleado(documento, nombre, apellido, categoria, sueldo, empresa);
                    
                    Empresa.empleados.add(emp); 
                    
                    jtDocumento.setText("");
                    jtNombre.setText("");
                    jtApellido.setText("");
                    jtSueldo.setText("");
                    
                    
                    JOptionPane.showMessageDialog(this, "Empleado registrado exitosamente...");
                    
                    
                    
            
            }catch(NumberFormatException e){
            
                JOptionPane.showMessageDialog(this, "Error en campo de texto, compruebe haber escrito bien..");
            
            }
        
        }
        
    }//GEN-LAST:event_jbGuardarActionPerformed

    private void jtcrearEmpresaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtcrearEmpresaActionPerformed
        
        try{
        
            String razonSocial = jtRazonSocial.getText();
            int cuit = Integer.parseInt(jtCuit.getText());
            
            if(jtRazonSocial.getText().isEmpty() && jtCuit.getText().isEmpty()){
        
            JOptionPane.showMessageDialog(this, "Rellene los campos de texto por favor: ");
            return;
            
            }else{
        
            Empresa emp = new Empresa(razonSocial, cuit);
            
            empresa.add(emp);
            
            jcEmpresas.addItem(emp);
            
            jbGuardar.setEnabled(rootPaneCheckingEnabled);
            jbMostrar.setEnabled(rootPaneCheckingEnabled);
            
            jtRazonSocial.setText("");
            jtCuit.setText("");
        
        }
        
        }catch(NumberFormatException e){
        
            JOptionPane.showMessageDialog(this, "Ingrese datos correctos: ");
        
        }  
        
    }//GEN-LAST:event_jtcrearEmpresaActionPerformed

